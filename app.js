let currentLang = "ar";

const setLanguage = (lang) => {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
  localStorage.setItem("mg-start-lang", lang);
};

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const savedLang = localStorage.getItem("mg-start-lang");
if (savedLang === "en") {
  setLanguage("en");
}

const header = document.querySelector(".site-header");
const setHeaderState = () => {
  header.classList.toggle("is-scrolled", window.scrollY > 10);
};
setHeaderState();
window.addEventListener("scroll", setHeaderState, { passive: true });

const menuToggle = document.querySelector(".menu-toggle");
menuToggle.addEventListener("click", () => {
  const isOpen = document.body.classList.toggle("menu-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

document.querySelectorAll(".nav-panel a, .nav-actions a").forEach((link) => {
  link.addEventListener("click", () => {
    document.body.classList.remove("menu-open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.14 }
);

document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));

const form = document.querySelector("#contactForm");

if (form) {
  const statusNode = form.querySelector(".form-status");
  const submitButton = form.querySelector("button[type='submit']");

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    statusNode.className = "form-status";

    if (!form.checkValidity()) {
      form.reportValidity();
      statusNode.textContent = translations[currentLang]["form.validation"];
      statusNode.classList.add("error");
      return;
    }

    const originalText = submitButton.textContent;
    submitButton.disabled = true;
    submitButton.textContent = translations[currentLang]["form.loading"];

    try {
      const formData = new FormData(form);
      const response = await fetch("https://api.web3forms.com/submit", {
        method: "POST",
        body: formData
      });
      const result = await response.json();

      if (!response.ok || !result.success) {
        throw new Error("Submission failed");
      }

      form.reset();
      statusNode.textContent = translations[currentLang]["form.success"];
      statusNode.classList.add("success");
    } catch (error) {
      statusNode.textContent = translations[currentLang]["form.error"];
      statusNode.classList.add("error");
    } finally {
      submitButton.disabled = false;
      submitButton.textContent = originalText;
    }
  });
}
